public class Main {
    public static void main(String[] args) {
        // Distance in kilometers
        double kilometers = 10.8;

        // Conversion rate
        double milesPerKm = 1.6;

        // Convert to miles
        double miles = kilometers * milesPerKm;

        // Output the result
        System.out.println("The distance " + kilometers + " km in miles is " + miles);
    }
}
